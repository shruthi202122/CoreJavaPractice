package com.pract.general;

public class Switch{
    public static void main(String[] arg){
        int x=2;
        switch(x){
            case 2:
                System.out.println("x = "+x);
                break;
            case 22:
                System.out.println("x = "+x);
                break;
            case 23:
                System.out.println("x = "+x);
                break;
        }
    }
}